def test_apply_helpers_import_shared_common_when_executed_from_app_tools(
    tmp_path,
) -> None:
    tools_dir = tmp_path / "app" / "tools"
    tools_dir.mkdir(parents=True)
    for name in ("apply_common.py", "sslfilter_apply.py", "webfilter_apply.py"):
        shutil.copy2(REPO_ROOT / "web" / "tools" / name, tools_dir / name)

    cases = (
        (
            "sslfilter_apply.py",
            "10-sslfilter.conf",
            "# Autogenerated: sslfilter apply failed\n",
        ),
        (
            "webfilter_apply.py",
            "30-webfilter.conf",
            "# Autogenerated: web filtering disabled (import failed)\n",
        ),
    )
    for script_name, output_name, safe_include in cases:
        out = tmp_path / output_name
        result = subprocess.run(
            [sys.executable, str(tools_dir / script_name), "--out", str(out)],
            cwd=tmp_path,
            check=False,
            capture_output=True,
            text=True,
        )

        assert result.returncode == 2
        assert out.read_text(encoding="utf-8") == safe_include


