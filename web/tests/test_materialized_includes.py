def test_sslfilter_apply_logs_safe_failure_when_materialization_fails(
    tmp_path, monkeypatch, capsys
) -> None:
    module = _import_tool_module("sslfilter_apply")
    out = tmp_path / "etc" / "squid" / "conf.d" / "10-sslfilter.conf"

    class BrokenStore:
        def __init__(self, *, squid_include_path: str) -> None:
            self.squid_include_path = squid_include_path

        def apply_squid_include(self) -> None:
            msg = "database socket unavailable"
            raise RuntimeError(msg)

    monkeypatch.setitem(
        sys.modules,
        "services.sslfilter_store",
        SimpleNamespace(SslFilterStore=BrokenStore),
    )

    assert module.main(["--out", str(out)]) == 3
    assert (
        out.read_text(encoding="utf-8") == "# Autogenerated: sslfilter apply failed\n"
    )
    payload = json.loads(capsys.readouterr().err)
    assert payload["helper"] == "sslfilter_apply"
    assert payload["event"] == "apply_failed"
    assert payload["error_type"] == "RuntimeError"
    assert payload["reason"] == "Operation failed. Check server logs for details."
    assert "database socket unavailable" not in payload["reason"]


